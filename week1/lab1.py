# Считать отдельными операторами 2 целых числа, получить: 
# сумму, разность, результат целочисленного деления, частное, остаток от деления, 
# третью степень первого числа и вторую степень второго числа и 
# вывести их отдельными операторами вывода.

a = int(input())
b = int(input())

print(a+b)
print(a-b)
print(a//b)
print(a/b)
print(a%b)
print(a**3)
